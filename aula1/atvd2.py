#Você está criando um programa para calcular a área de um retângulo. O programa deve solicitar ao usuário que forneça a base e
#  a altura do retângulo. Em seguida, o programa deve calcular a área usando a fórmula:

#Área=Base×Altura

#O programa deve exibir a área calculada.

base = float(input("Forneça a base do seu retângulo: "))
altura = float(input("Forneça a altura do seu retângulo: "))

area = base*altura

print(f"A área do seu retângulo é: {area}")